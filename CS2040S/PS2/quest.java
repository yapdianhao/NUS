import java.util.*;

class Quest {

    int energy;
    int gold;
    int id;

    public Quest(int energy, int gold) {
        this.energy = energy;
        this.gold = gold;
    }
    public Quest(int energy, int gold, int id) {
        this.energy = energy;
        this.gold = gold;
        this.id = id;
    }
     
}